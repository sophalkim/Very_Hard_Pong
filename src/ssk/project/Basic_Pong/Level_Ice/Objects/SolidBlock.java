package ssk.project.Basic_Pong.Level_Ice.Objects;

public class SolidBlock {
	
	public int solidBlockState = 1;
	public int solidBlockX;
	public int solidBlockY;
	public int solidBlockWidth;
	public int solidBlockHeight;
	
	public int getSolidBlockState() {
		return solidBlockState;
	}
	public void setSolidBlockState(int solidBlockState) {
		this.solidBlockState = solidBlockState;
	}
	public int getSolidBlockX() {
		return solidBlockX;
	}
	public void setSolidBlockX(int solidBlockX) {
		this.solidBlockX = solidBlockX;
	}
	public int getSolidBlockY() {
		return solidBlockY;
	}
	public void setSolidBlockY(int solidBlockY) {
		this.solidBlockY = solidBlockY;
	}
	public int getSolidBlockWidth() {
		return solidBlockWidth;
	}
	public void setSolidBlockWidth(int solidBlockWidth) {
		this.solidBlockWidth = solidBlockWidth;
	}
	public int getSolidBlockHeight() {
		return solidBlockHeight;
	}
	public void setSolidBlockHeight(int solidBlockHeight) {
		this.solidBlockHeight = solidBlockHeight;
	}
}
